void print_correct(std::string password, std::vector<int> correct_guesses) {
  bool printed;
  for (int i; i <= password.size(); i++) {
    for (int ii; ii <= correct_guesses.size(); ii++) {
      if (i == correct_guesses[ii]) {
        std::cout << password[i];
        printed = true;
      }
    }
    if (printed == false) {
      std::cout << "_";
    }
  }
  std::cout << std::endl;
}
