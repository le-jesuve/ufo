void print_correct(std::string password, std::vector<int> correct) {
  //  bool printed = false;
  int pind = 0;
  int cind = 0;
  for (char i : password) {
    bool printed = false;
    for (int ii : correct) {
      if (pind == ii) {

        std::cout << i;
        printed = true;
      }
      ++cind;
    }
    if (printed == false) {
      std::cout << "_";
    }
    ++pind;
  }
}
